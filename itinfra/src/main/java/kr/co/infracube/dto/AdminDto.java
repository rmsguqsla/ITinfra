package kr.co.infracube.dto;

public class AdminDto {
	private String Email;
	private String Password;
	private String AName;
	private String Note;
	private String Use_YN;
	private String Insert_Admin;
	private String Insert_Date;
	private String Update_Admin;
	private String Update_Date;
	
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	public String getAName() {
		return AName;
	}
	public void setAName(String aName) {
		AName = aName;
	}
	public String getNote() {
		return Note;
	}
	public void setNote(String note) {
		Note = note;
	}
	public String getUse_YN() {
		return Use_YN;
	}
	public void setUse_YN(String use_YN) {
		Use_YN = use_YN;
	}
	public String getInsert_Admin() {
		return Insert_Admin;
	}
	public void setInsert_Admin(String insert_Admin) {
		Insert_Admin = insert_Admin;
	}
	public String getInsert_Date() {
		return Insert_Date;
	}
	public void setInsert_Date(String insert_Date) {
		Insert_Date = insert_Date;
	}
	public String getUpdate_Admin() {
		return Update_Admin;
	}
	public void setUpdate_Admin(String update_Admin) {
		Update_Admin = update_Admin;
	}
	public String getUpdate_Date() {
		return Update_Date;
	}
	public void setUpdate_Date(String update_Date) {
		Update_Date = update_Date;
	}
	
	
}
